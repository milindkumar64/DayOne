package com.bridgelabz.whileloop;

import java.util.Scanner;

public class SumOfNaturalNumbers {

	public static void main(String[] args) {
		
	
         int n = 5;
         int i=1;
         int sum =0;
         while(i<=n)
         {
        	
        	 sum=sum+i;
        	 i++;
         }
         System.out.println("value of sum :"+sum);
	}

}